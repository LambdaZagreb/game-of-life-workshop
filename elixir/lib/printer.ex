defmodule ConwayPrinter do
  def play(_grid, _name, 0), do: :ok
  def play(grid, name, steps) do
    grid
    |> print(name)
    |> Conway.next
    |> play(name, steps - 1)
  end

  defp print(grid, name) do
    IO.puts(String.duplicate("\n", 100))
    IO.puts("#{name}\n")

    print_grid(grid)

    :timer.sleep(200)

    grid
  end

  defp print_grid(grid) do
    for y <- 0..(Conway.size(grid) - 1) do
      for x <- 0..(Conway.size(grid) - 1) do
        case Conway.cell_status(grid, x, y) do
          0 -> IO.write(" ")
          1 -> IO.write("*")
        end
      end
      IO.puts("")
    end
  end

  def play_all do
    for {name, {steps, data}} <- patterns do
      Conway.new(data)
      |> play(name, steps)
    end
    :ok
  end

